class Animal:
    name = "DOG"
    def __init__(self, name, age, birth):
        self.name = name
        self.age = age 
        self.birth = birth 

    def speak(self):
        return f"{self.name} {self.age} {self.birth} makes a sound"
    print("name value")

cat = Animal("Cat","23","2022")
print(cat.speak()) 

